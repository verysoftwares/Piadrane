function green(i)
    fg((0.3+i*0.15)*255,(0.45+i*0.15)*255,(0.4+i*0.15)*255)
end
function purple(i)
    fg((0.45+i*0.15)*255,(0.3+i*0.15)*255,(0.4+i*0.15)*255)
end
function blue(i)
    fg((0.3+i*0.15)*255,(0.4+i*0.15)*255,(0.45+i*0.15)*255)
end
function yellow(i)
    fg((0.45+i*0.15)*255,(0.45+i*0.15)*255,(0.3+i*0.15)*255)
end
